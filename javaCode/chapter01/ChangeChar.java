public class ChangeChar{
	public static void main(String[] args){
		System.out.println("北京\t上海");
		System.out.println("北京\n上海");
		System.out.println("C:\\Windows\\System32\\cmd.exe");
		// 回车\r 光标回到左边
		System.out.println("北京玩\r上海");
		
		System.out.println("1\t1\t1\t1");
		System.out.println("22\t22\t22\t22");
		System.out.println("333\t333\t333\t333");
		
		
	}
}